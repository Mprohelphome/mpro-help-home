export default async function handler(
  req: NextApiRequest,
  res: NextApiResponse<any>
  ) {
    try {

      const date = new Date()
      const day = date.getDate().toString().padStart(2, '0')
      const month = (date.getMonth() + 1).toString().padStart(2, '0') // Mês começa do 0 (janeiro) até 11 (dezembro), por isso adicionamos 1 aqui
      const year = date.getFullYear().toString()
      const formattedDate = `${day}/${month}/${year} às ${date.getHours()}:${date.getMinutes()}`

      const data : IBody = req.body;
      let content = template;

      content = content.replace('{{NOME}}', data.name);
      content = content.replace('{{TELEFONE}}', data.cellphone);
      content = content.replace('{{EMAIL}}', data.email);
      content = content.replace('{{CEP}}', data.cep);
      content = content.replace('{{DESCRICAO}}', data.description);

      const email = "albertoaraujo@mprohelphome.com.br";
       const transporter = nodemailer.createTransport({
         host: 'smtppro.zoho.com',
        port: 465,
        secure: true,
        auth: {
          user: email,
          pass: "Alaf2024",
        },
      });

      const mailOptions = {
          from: "albertoaraujo@mprohelphome.com.br",
          to: email,
          subject: 'Olá eu sou ' + data.name + ' e gostaria de um atendimento.',
          html: content,
        };

    
      const result = await transporter.sendMail(mailOptions);

      return res.status(200).json({ success: true, result })
    } catch (error) {
      console.log(error);

      return res.status(200).json({ success: true })
    }
}
